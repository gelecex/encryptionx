public class DSSymmetricEncryptionException extends Exception {

    public DSSymmetricEncryptionException() {
        super();
    }

    public DSSymmetricEncryptionException(String message) {
        super(message);
    }

    public DSSymmetricEncryptionException(Throwable cause) {
        super(cause);
    }

    public DSSymmetricEncryptionException(String message, Throwable cause) {
        super(message, cause);
    }
}

